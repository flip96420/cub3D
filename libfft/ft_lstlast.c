#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*current;

	current = lst;
	if (!lst)
		return (NULL);
	while (current->next != NULL)
		current = current->next;
	current->next = NULL;
	return (current);
}
